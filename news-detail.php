				Департамент научно-технических программ и проектов Госкорпорации «Росатом» c 23 по 25 октября на базе Московского филиала АНО ДПО «Техническая академия Росатома» проведет второй этап конкурса «Инновационный лидер атомной отрасли» – Школа «Инновационный лидер».<br><br>23 октября победители первого этапа примут участие в проектной сессии «Анализ трендов и прогнозов, возможностей и рисков для развития атомной отрасли» и Центре оценки.<br><br>
Российский тележурналист, медиатренер, академик евразийской телевизионной академии, пресс-секретарь Генерального директора АО АККУЮ НУКЛЕАР Василий Корельский проведет мастер – класс «Успех в публичных выступлениях».<br><br>
Второй день школы будет посвящен защите проектов и подведению итогов конкурса, а завершится экскурсией в музей кабинет советского атомщика Ефима Славского, много лет руководившего Министерством среднего машиностроения СССР. Кабинет Славского в Госкорпорации «Росатом» до сих пор сохранился практически в том же виде, в каком он был при Ефиме Павловиче.<br><br>
В третий день школы состоится технический тур в лаборатории АО «НПК «Химпроминжиниринг», затем участники погрузятся в проблематику Customer development в инновационных проектах на мастер- классе в пространстве «Среда» Москва-Сити.<br><br>
<h3>Справка:</h3>
Целью проведения конкурса и присуждения премий молодым работникам атомной отрасли, проявившим активность в области разработки и внедрения инновационных проектов, является стимулирование инновационной активности, внедрение проектной культуры в решении научных и инновационных задач. <br><br>
Участники конкурса: молодые работники организаций атомной отрасли в возрасте до 35 лет, участвующие в разработке и реализации инновационных проектов.<br><br>
Конкурс проводится ежегодно в два этапа. В ходе первого этапа (заочного) Конкурсная комиссия проводит экспертизу и оценку поступивших на конкурс материалов.<br><br>
